package com.oocl.todolistapi.dto;

public class TodoRequest {
    private String text;
    private boolean isDone;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public boolean isDone() {
        return isDone;
    }

    public void setDone(boolean done) {
        isDone = done;
    }
}
